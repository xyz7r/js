/*

assignment operator

= equal
+ addition
- subtraction
/ division
* multiplication

*/

// equal
var myVar = 9;
console.log("My Variable", myVar); //output : 9

// addtion
var addition = myVar + 10;
console.log("addition", addition); //output : 19

// subtraction
var subtraction = myVar - 10;
console.log("subtraction", subtraction); //output : -1

// division
var division = myVar / 10;
console.log("division", division); //output : 0.9

// multiplication
var multiplication = myVar * 10;
console.log("multiplication", multiplication); //output : 90

console.log("===========================================================");

// Math Operator Short-Hand

// equal
var myVar = 9;
console.log("My Variable", myVar); //output : 9

// addtion
myVar += 10;
console.log("addition Short-Hand", addition); //output : 19

// subtraction
myVar -= 10;
console.log("subtraction Short-Hand", subtraction); //output : -1

// division
myVar /= 10;
console.log("division Short-Hand", division); //output : 0.9

// multiplication
myVar *= 10;
console.log("multiplication Short-Hand", multiplication); //output : 90


console.log("===========================================================");
console.log("My Variable", myVar); //output : 9
// Increment Operator
// Postfix
myVar++
console.log("Postfix Increment ", myVar); //output : 10

//Prefix
++myVar
console.log("Prefix Increment ", myVar); //output : 11

// Decrement Operator
// Postfix
myVar--
console.log("Postfix Decrement ", myVar); //output : 10

//Prefix
--myVar
