/*

assignment operator

= equal
+ addition
- subtraction
/ division
* multiplication

*/

// equal
var myVar = 9;
console.log("My Variable", myVar);

// addtion
var addition = myVar + 10;
console.log("addition", addition);

// subtraction
var subtraction = myVar - 10;
console.log("subtraction", subtraction);

// division
var division = myVar / 10;
console.log("division", division);

// multiplication
var multiplication = myVar * 10;
console.log("multiplication", multiplication);

console.log("===========================================================");

// Math Operator Short-Hand

// equal
var myVar = 9;
console.log("My Variable", myVar);

// addtion
myVar += 10;
console.log("addition Short-Hand", addition);

// subtraction
myVar -= 10;
console.log("subtraction Short-Hand", subtraction);

// division
myVar /= 10;
console.log("division Short-Hand", division);

// multiplication
myVar *= 10;
console.log("multiplication Short-Hand", multiplication);


console.log("===========================================================");
